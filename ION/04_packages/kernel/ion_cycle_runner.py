"""Deterministic ION carrier-cycle planner.

This module closes the gap exposed by Cursor carrier operation: the carrier must
not decide whether to run kernel-only, all roles, or arbitrary Tasks from chat.
It must ask ION for the cycle plan, then execute only the generated role-session
packets.

The module does not call Cursor's Task API. It emits a carrier-executable spawn
plan and an execution bundle generated by the existing SequentialKernelRouter.
Cursor or any other carrier must treat that plan as authority.
"""

